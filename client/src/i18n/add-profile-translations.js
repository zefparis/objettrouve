// Fonction pour définir une valeur dans un objet imbriqué
function setNestedValue(obj, key, value) {
  const keys = key.split('.');
  const last = keys.pop();
  const target = keys.reduce((current, part) => {
    if (!current[part]) current[part] = {};
    return current[part];
  }, obj);
  target[last] = value;
}

// Ajouter les traductions pour chaque langue
LANGUAGES.forEach(lang => {
  const filePath = path.join(__dirname, 'locales', lang, 'translation.json');
  
  try {
    const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    const translations = profileTranslations[lang];
    
    if (translations) {
      // Créer la section profile si elle n'existe pas
      if (!existing.profile) {
        existing.profile = {};
      }
      
      // Ajouter chaque traduction en évitant les conflits
      Object.keys(translations).forEach(key => {
        const keyParts = key.split('.');
        if (keyParts.length > 1) {
          // Retirer 'profile.' du début
          const profileKey = keyParts.slice(1).join('.');
          setNestedValue(existing.profile, profileKey, translations[key]);
        }
      });
      
      fs.writeFileSync(filePath, JSON.stringify(existing, null, 2));
      console.log(`✅ ${lang.toUpperCase()}: ${Object.keys(translations).length} clés de profil ajoutées`);
    }
  } catch (error) {
    console.error(`❌ ${lang.toUpperCase()}: Erreur lors du traitement - ${error.message}`);
  }
});

