// Fonction pour mettre à jour les traductions
function updateTranslations() {
  console.log('🔄 Mise à jour forcée des traductions profilePage...');
  
  LANGUAGES.forEach(lang => {
    const filePath = path.join(__dirname, 'locales', lang, 'translation.json');
    
    try {
      // Lire le fichier existant
      const existingContent = JSON.parse(fs.readFileSync(filePath, 'utf8'));
      
      // Supprimer l'ancienne section profilePage si elle existe
      if (existingContent.profilePage) {
        delete existingContent.profilePage;
      }
      
      // Ajouter la nouvelle section profilePage
      existingContent.profilePage = profileTranslations[lang];
      
      // Écrire le fichier avec un formatage propre
      fs.writeFileSync(filePath, JSON.stringify(existingContent, null, 2) + '\n');
      
      console.log(`✅ ${lang.toUpperCase()}: Traductions profilePage mises à jour (${Object.keys(profileTranslations[lang]).length} sections)`);
      
    } catch (error) {
      console.error(`❌ ${lang.toUpperCase()}: Erreur lors de la mise à jour - ${error.message}`);
    }
  });
  
  console.log('\n🎉 Mise à jour complète terminée pour toutes les langues!');
  console.log('🔄 Redémarrez l\'application pour prendre en compte les nouvelles traductions.');
}

// Exécuter la mise à jour
updateTranslations();